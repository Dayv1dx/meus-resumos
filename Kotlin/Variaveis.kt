fun main(args : Array<String>){

// "var" é uma variável
var usuario = "Dayv1dx"

// Se tratando de números, quando não informado o tipo, o padrão é um Double
var decimal = 3.5
var float = 3.5f
val long = 124121342L
val int = 2342342

// Outros tipos
val string = "string"
val boolean = true

// "val" vem de 'value', ou seja, é um valor constante que não pode ser alterado.
val pi = 3.14

println( usuario )

var salario = 300
var bonus = 200

var total = salario + bonus

// Usando o símbolo cifrão "$" é possível printar o valor de uma variavél dentro de uma String
println("O salário é $total")

}